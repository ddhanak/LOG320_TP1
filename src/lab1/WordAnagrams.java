package lab1;

public class WordAnagrams {
	
	private String wordName;
	private int count;
	
	public WordAnagrams(String wordName, int count) {
		this.wordName = wordName;
		this.count = count;
	}
	
	public String WordName(){
		return this.wordName;
	}
	public int Count(){
		return this.count;
	}


}
